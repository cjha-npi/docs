  // Initialize vars.htmlData array
  async function initHtmlData() {

    // Reset the HTML data before rebuilding it, while maintaining its reference
    vars.htmlData.length = 0;

    // Check whether the page should be redirected.
    function checkRedirect(tree) {
      // tree: Array with nodes in the shape [label, scope, hrefOrKids].
      // The href stored here is the default NAVTREE href generated by Doxygen,
      // for example: d2/d0d/classnpi_1_1_ref_ptr.html.
      // We match only the base name, for example: classnpi_1_1_ref_ptr.

      // Gate 1: Only proceed on a truly fresh tab (not after reload/back/forward)
      const isStale = (sessionStorage.getItem('is_stale') === 'true');
      sessionStorage.setItem('is_stale', 'true');
      if (isStale) return false; // Not a fresh load: no redirect

      // Gate 2: Only on standard navigation (ignore reloads and history traversals)
      const navType = performance.getEntriesByType('navigation')[0]?.type ?? 'navigate';
      if (navType !== 'navigate') return false; // Reload or back/forward: no redirect

      // Gate 3: Only redirect on landing page (root or 'index.html')
      const pName = window.location.pathname;
      if (!pName.endsWith('/') && !pName.endsWith('/index.html')) return false; // Not on landing page: no redirect

      // Gate 4: Stored HTML must exist, be non-empty, and not be the current HTML name
      const htmlPrevName = storage.htmlPrevName.load();
      if (!htmlPrevName
        || typeof htmlPrevName !== 'string'
        || htmlPrevName.length === 0
        || htmlPrevName === CONSTS.HTML_NAME) return false; // No redirect

      // Find a matching HTML page name.
      function findLastHtmlPage(branch) {
        if (!Array.isArray(branch) || branch.length === 0) return false;
        for (const node of branch) {
          if (typeof node[2] === 'string') {
            const name = node[2].split('/').pop().replace(/\.[^.]*$/, '');
            if (name === htmlPrevName) {
              window.location.replace(CONSTS.DOC_ROOT + node[2]); // Redirect
              return true; // Stop after the first match
            }
          }
          else if (Array.isArray(node[2])) {
            if (findLastHtmlPage(node[2])) return true; // Propagate success upward
          }
        }
        return false;
      }

      // Return whether a matching page was found.
      return findLastHtmlPage(tree);
    }

    // Load previous HTML data if available
    const prevHtmlData = storage.htmlData.load();

    // Check for previous HTML data being a non-empty array
    if (Array.isArray(prevHtmlData) && prevHtmlData.length > 0) {

      // Check for redirection, if true return because the url will be redirected
      if (checkRedirect(prevHtmlData)) return;

      // Add dev hub class in document body to detect it in CSS file
      if (prevHtmlData[prevHtmlData.length - 1][0] === 'Dev Hub') {
        document.body.classList.add(CONSTS.CLS.DEV_HUB_AVAILABLE);
      }

      // Extend expiry timeout of ignored not found HTML arrays
      storage.htmlIgnored.extendExpiry();
      storage.htmlNotFound.extendExpiry();

      // Save current HTML name for future use
      storage.htmlPrevName.save(CONSTS.HTML_NAME);

      // Assign loaded HTML data to vars.htmlData array, freeze it and return
      vars.htmlData.push(...prevHtmlData);
      deepFreeze(vars.htmlData);
      return;
    }

    // Get the fully built pruned Doxygen NAVTREE
    const prunedNavTree = await buildPrunedDoxygenNavTree();

    // Check whether prunedNavTree is an array
    if (!Array.isArray(prunedNavTree)) {
      dispFatalError('initHtmlData(): Pruned Doxygen NAVTREE is not an array.');
      return;
    }

    // Check whether prunedNavTree is empty
    if (prunedNavTree.length === 0) {
      dispFatalError('initHtmlData(): Pruned Doxygen NAVTREE is empty.');
      return;
    }

    // For prunedNavTree these are guaranteed:
    // - node[0] is a non-empty string.
    // - node[1] is either null or a non-empty string ending in .html.
    // - node[2] is either null or an array of valid child nodes.
    // - At least one of node[1] and node[2] is non-null.
    // So, we will be reducing the number of redundant checks from here onwards.

    // Map of all nodes that is to be obtained from prunedNavTree
    const pending = new Map([
      ['mainPage', { Link: 'index.html', Path: [] }],
      ['depRequired', { Link: 'dp_dep_required.html', Path: [] }],
      ['dependencies', { Link: 'dp_dep_dir_dependencies.html', Path: [] }],
      ['namespaceList', { Link: 'namespaces.html', Path: ['Namespaces', 'Namespace List'] }],
      ['namespaceMems', { Link: null, Path: ['Namespaces', 'Namespace Members'] }],
      ['concepts', { Link: 'concepts.html', Path: ['Concepts'] }],
      ['classList', { Link: 'annotated.html', Path: ['Classes', 'Class List'] }],
      ['classMems', { Link: null, Path: ['Classes', 'Class Members'] }],
      ['classHierarchy', { Link: 'hierarchy.html', Path: ['Classes', 'Class Hierarchy'] }],
      ['classIndex', { Link: 'classes.html', Path: ['Classes', 'Class Index'] }],
      ['fileList', { Link: 'files.html', Path: ['Files', 'File List'] }],
      ['fileMems', { Link: null, Path: ['Files', 'File Members'] }],
      ['examples', { Link: 'examples.html', Path: ['Examples'] }],
      ['devHub', { Link: 'dp_dh_dir_dev_hub.html', Path: [] }],
      ['dhRequired', { Link: 'dp_dh_required.html', Path: [] }],
      ['deprList', { Link: 'deprecated.html', Path: ['Deprecated List'] }],
      ['bugsList', { Link: 'bug.html', Path: ['Bug List'] }],
      ['todoList', { Link: 'todo.html', Path: ['Todo List'] }],
      ['testList', { Link: 'test.html', Path: ['Test List'] }],
    ]);

    // Traverse prunedNavTree and resolve matching pending entries.
    const found = (() => {

      // Result object that will be returned and assigned to found object.
      const obj = Object.create(null);

      // If the pending Map is empty, display a fatal error and return the empty object.
      if (pending.size === 0) {
        dispFatalError('initHtmlData(): The pending Map is empty, there is nothing to find in prunedNavTree.');
        return obj;
      }

      // Returns true if 'need' exactly matches the trailing part of 'have'.
      // In other words, 'need' must appear as a contiguous suffix of 'have'.
      // Examples:
      // - have = ['Z', 'A', 'B', 'C'], need = ['A', 'B', 'C'] -> true
      // - have = ['A', 'B', 'C'],      need = ['A', 'B', 'C'] -> true
      // - have = ['Z', 'Y', 'A', 'B', 'C'], need = ['A', 'B', 'C'] -> true
      // - have = ['Z', 'A', 'M', 'B', 'C'], need = ['A', 'B', 'C'] -> false
      // Here, 'have' is the currently available path and 'need' is the required
      // path suffix stored in the pending Map.
      function matchPath(have, need) {
        if (need.length === 0) return true;
        if (have.length < need.length) return false;
        const beg = have.length - need.length;
        for (let ii = 0; ii < need.length; ++ii) {
          if (have[ii + beg] !== need[ii]) return false;
        }
        return true;
      }

      // FIFO work queue used to traverse prunedNavTree and search for pending items.
      const queue = [];

      // Adds a prunedNavTree node to the queue.
      // 'path' contains the names of the node's ancestors.
      // The queue stores only the last href segment. For example, if a node href is
      // "A/B/c.html", the stored value is "c.html". This allows matching by HTML file
      // name only. Doxygen-generated HTML names already encode the ancestor context
      // (such as namespace and class), so matching the full path is unnecessary and
      // would only reflect the folder structure.
      function addToQueue(node, path = []) {
        const href = node[1] ? node[1].split('/').pop() : null;
        queue.push({ Node: node, Link: href, Path: path.concat(node[0]) });
      }

      // Add the top-level nodes of prunedNavTree to the queue.
      for (const node of prunedNavTree) addToQueue(node);

      // Current queue index acting as the FIFO head.
      let head = 0;

      // While the queue still has items and the pending Map is not empty,
      // try to match queued nodes against pending entries. Each node is expected
      // to satisfy at most one pending entry, so stop after the first match.
      while (head < queue.length && pending.size > 0) {
        const item = queue[head++];
        for (const [key, val] of pending) {
          if (matchPath(item.Path, val.Path) && (val.Link === null || val.Link === item.Link)) {
            obj[key] = item.Node;
            pending.delete(key);
            break;
          }
        }

        // Since prunedNavTree is already pruned and normalized, if node[2] exists 
        // then it has to be a non-empty branch.
        if (item.Node[2]) {
          for (const node of item.Node[2]) addToQueue(node, item.Path);
        }
      }

      return obj;
    })();

    // Remaining pending entries
    const htmlNotFound = [];
    if (pending.size > 0) {
      for (const [, val] of pending) htmlNotFound.push([val.Path.join(' > '), val.Link]);
    }

    // List of nodes whose hrefs are deliberately ignored.
    const ignoredList = [];
    const seenIgnoredHref = new Set();

    // Build and return a NEW branch for vars.htmlData from a branch of prunedNavTree.
    function convertBranch(branch, parentChain, ...regexList) {
      // Output node format:
      // - [label, null, kids] for branches
      // - [label, null, href] for leaves
      // Rules:
      // 1. Traverse the input branch recursively.
      // 2. Keep a branch only if at least one converted child remains.
      // 3. Keep each leaf href at most once per call.
      // 4. If regex filters are given, keep only leaves whose href base name
      //    (file name without path or extension) matches at least one regex.
      // 5. If a node has both href and kept children, keep it only as a branch
      //    and record its href in ignoredList once.
      // The parentChain is used only to build readable paths such as
      // "A > B > C" for ignored-link reporting.

      // Invalid or empty input branch produces an empty result branch.
      if (!Array.isArray(branch) || branch.length === 0) return [];

      // All extra arguments must be RegExp objects.
      // On failure, report a fatal error and return an empty branch.
      for (const regex of regexList) {
        if (!(regex instanceof RegExp)) {
          dispFatalError('initHtmlData() - convertBranch(): Invalid Regex found in regexList parameter', regex);
          return [];
        }
      }

      // Returns true if the href base name matches any regex in regexList.
      // Example: "a/b/class_foo.html" -> "class_foo".
      function testHrefBase(fullHref) {
        const baseHref = fullHref.split('/').pop();
        const dotPos = baseHref.lastIndexOf('.');
        const nameHref = (dotPos >= 0) ? baseHref.slice(0, dotPos) : baseHref;
        for (const regex of regexList) {
          // Reset lastIndex before each test for /g and /y regex safety.
          regex.lastIndex = 0;
          if (regex.test(nameHref)) return true;
        }
        return false;
      }

      // Tracks hrefs already emitted into the returned branch.
      // This prevents duplicate leaf entries within this call.
      const seen = new Set();

      // Recursively convert one sub-branch into the vars.htmlData format.
      // The curParChain is only used for ignored-link reporting.
      function convert(subBranch, curParChain) {
        if (!Array.isArray(subBranch) || subBranch.length === 0) return [];
        const rows = [];
        for (const node of subBranch) {

          // Human-readable full path for diagnostics / ignored-list reporting.
          // curParChain("A > B") + node[0] ("C") => name ("A > B > C")
          const name = curParChain ? `${curParChain} > ${node[0]}` : node[0];

          // Convert children first to decide whether this node survives as a branch.
          const kids = node[2] ? convert(node[2], name) : [];

          if (kids.length > 0) {
            // Branch case: keep children and ignore the node href in output.
            // Record that ignored href once if it exists.
            if (node[1] && !seen.has(node[1]) && !seenIgnoredHref.has(node[1])) {
              ignoredList.push([name, node[1]]);
              seenIgnoredHref.add(node[1]);
            }
            rows.push([node[0], null, kids]);
          }
          else if (node[1] && !seen.has(node[1])) {
            // Leaf case: keep only unique hrefs.
            // If filters exist, the href base name must match one of them.
            seen.add(node[1]);
            if (regexList.length === 0 || testHrefBase(node[1])) {
              rows.push([node[0], null, node[1]]);
            }
          }

          // Otherwise skip the node.
        }
        return rows;
      }

      // Convert the input branch and return the new vars.htmlData branch.
      return convert(branch, parentChain);
    }

    // Group a flat converted list into alphabetic buckets and return the NEW list.
    function putInAlphabeticBuckets(flatList) {
      // Input:
      // - flatList is expected to be a converted flat list where each item is a
      //   node in the form [label, null, hrefOrKids].
      // Output:
      // - Returns a NEW list of bucket nodes in the form [bucketLabel, null, kids].
      // Rules:
      // 1. Group items by the uppercased first character of node[0].
      // 2. If that character is between 'A' and 'Z', use it as the bucket name.
      // 3. All other starting characters go into the 'Other' bucket.
      // 4. Buckets are emitted in fixed order: 'A' to 'Z', then 'Other'.
      // 5. Empty or invalid input returns an empty list.

      // Invalid or empty input produces an empty result.
      if (!Array.isArray(flatList) || flatList.length === 0) return [];

      // Map each bucket name to the list of nodes that belong to it.
      const map = new Map();
      for (const node of flatList) {
        const firstChar = node[0][0].toUpperCase();
        const code = firstChar.charCodeAt(0);
        const key = (code >= 65 && code <= 90) ? firstChar : 'Other';
        if (!map.has(key)) map.set(key, []);
        map.get(key).push(node);
      }

      // Build the final bucketed output list.
      const out = [];

      // Emit alphabetic buckets in fixed A-Z order.
      for (let cc = 65; cc <= 90; ++cc) {
        const key = String.fromCharCode(cc);
        const kids = map.get(key);
        if (kids && kids.length > 0) {
          out.push([key, null, kids]);
        }
      }

      // Emit the non-alphabetic bucket last if it has any items.
      const otherKids = map.get('Other');
      if (otherKids && otherKids.length > 0) {
        out.push(['Other', null, otherKids]);
      }

      return out;
    }

    // The main top nodes for the doxy-plus root pane.
    const theProject = ['Project', null, []];
    const theDependencies = ['Dependencies', null, []];
    const theNamespaces = ['Namespaces', null, []];
    const theConcepts = ['Concepts', null, []];
    const theClasses = ['Classes', null, []];
    const theFiles = ['Files', null, []];
    const theExamples = ['Examples', null, []];
    const theDevHub = ['Dev Hub', null, []];

    // Main 'index.html' page as overview page
    if (found.mainPage) theProject[2].push(['Overview', null, found.mainPage[1]]);

    // Dependency Required Page
    if (found.depRequired) theDependencies[2].push([found.depRequired[0], null, found.depRequired[1]]);

    // Dependencies
    if (found.dependencies) {
      seenIgnoredHref.add(found.dependencies[1]);
      ignoredList.push([found.dependencies[0], found.dependencies[1]]);
      if (theDependencies[2].length === 0) {
        theDependencies[2] = convertBranch(found.dependencies[2], 'Dependencies', /^dp_dep_/);
      }
      else {
        const tempDepList = convertBranch(found.dependencies[2], 'Dependencies', /^dp_dep_/);
        theDependencies[2].push(...tempDepList);
      }
    }

    // Namespace List
    if (found.namespaceList) {

      // Add main page to project node
      theProject[2].push(['Namespace List', null, found.namespaceList[1]]);

      // Populate
      (function processNamespaceListBranch(branch, path = null) {
        if (!Array.isArray(branch) || branch.length === 0) return;

        // Separate normal and anonymous namespace first, on same
        // level anonymous namespace always goes below normal ones.
        const nsNormList = [];
        const nsAnonList = [];
        for (const node of branch) {
          const href = node[1] ? node[1].split('/').pop() : null;
          if (href && /^namespace/.test(href)) {
            const pName = prettierAnon(node[0]);
            if (pName === node[0]) nsNormList.push([pName, node[1], node[2]]);
            else nsAnonList.push([pName, node[1], node[2]]);
          }
        }

        // Push normal namespace to namespace list branch.
        for (const node of nsNormList) {
          theNamespaces[2].push([node[0], path, node[1]]);
          if (node[2]) {
            const childPath = path ? `${path}::${node[0]}` : node[0];
            processNamespaceListBranch(node[2], childPath);
          }
        }

        // Push anonymous namespace to namespace list branch.
        for (const node of nsAnonList) {
          theNamespaces[2].push([node[0], path, node[1]]);
          if (node[2]) {
            const childPath = path ? `${path}::${node[0]}` : node[0];
            processNamespaceListBranch(node[2], childPath);
          }
        }

      })(found.namespaceList[2]);
    }

    // Namespace Members
    if (found.namespaceMems && found.namespaceMems[2]) {
      const converted = convertBranch(found.namespaceMems[2], 'Namespaces > Namespace Members');
      if (converted.length > 0) theNamespaces[2].push(['[Members]', null, converted]);
    }

    // Concepts
    if (found.concepts) {

      // Add main page to project node
      theProject[2].push(['Concepts List', null, found.concepts[1]]);

      // Populate
      (function processConceptsBranch(branch, prefix = null) {
        if (!Array.isArray(branch) || branch.length === 0) return;
        for (const node of branch) {
          const pName = prettierAnon(node[0]);

          const href = node[1] ? node[1].split('/').pop() : null;
          if (href && /^concept/.test(href)) {
            theConcepts[2].push([pName, prefix, node[1]]);
          }

          if (node[2]) {
            const childPrefix = prefix ? `${prefix}::${pName}` : pName;
            processConceptsBranch(node[2], childPrefix);
          }
        }
      })(found.concepts[2]);

      // Sort
      theConcepts[2].sort((a, b) => {

        // 1. Concept name.
        let cmpResult = a[0].localeCompare(b[0], undefined, { sensitivity: 'base' });
        if (cmpResult !== 0) return cmpResult;

        // 2. Path, but keep alphabetic namespaces before <file.cpp>-style ones.
        cmpResult = compareAlphaBeforePunctuation(a[1] || '', b[1] || '');
        if (cmpResult !== 0) return cmpResult;

        // 3. Final tie-breaker.
        return a[2].localeCompare(b[2], undefined, { sensitivity: 'base' });
      });

      // Change to alphabetic buckets if needed
      if (theConcepts[2].length > CONSTS.VAL.BUCKET_THRESHOLD) {
        theConcepts[2] = putInAlphabeticBuckets(theConcepts[2]);
      }
    }

    // Classes
    if (found.classList) {

      // Add main page to project node
      theProject[2].push(['Class List', null, found.classList[1]]);

      // Populate
      (function processClassListBranch(branch, namespacePath = null, classPath = null) {
        if (!Array.isArray(branch) || branch.length === 0) return;
        for (const node of branch) {

          let namespaceName = namespacePath;
          let className = classPath;

          const href = node[1] ? node[1].split('/').pop() : null;
          if (href) {
            const pName = prettierAnon(node[0]);
            if (/^(?:class|struct)/.test(href)) {
              className = classPath ? `${classPath}::${pName}` : pName;
              theClasses[2].push([className, namespaceName, node[1]]);
            }
            else if (/^namespace/.test(href)) {
              namespaceName = namespacePath ? `${namespacePath}::${pName}` : pName;
            }
          }

          if (node[2]) processClassListBranch(node[2], namespaceName, className);
        }
      })(found.classList[2]);

      // Sort
      theClasses[2].sort((a, b) => {
        // split class names
        const aClasses = a[0].split('::');
        const bClasses = b[0].split('::');

        // 1. Outermost class name.
        let cmpResult = aClasses[0].localeCompare(bClasses[0], undefined, { sensitivity: 'base' });
        if (cmpResult !== 0) return cmpResult;

        // 2. Namespace path, but keep alphabetic namespaces before <file.cpp>-style ones.
        cmpResult = compareAlphaBeforePunctuation(a[1] || '', b[1] || '');
        if (cmpResult !== 0) return cmpResult;

        // 3. Inner class segments.
        const minLen = Math.min(aClasses.length, bClasses.length);
        for (let ii = 1; ii < minLen; ++ii) {
          cmpResult = aClasses[ii].localeCompare(bClasses[ii], undefined, { sensitivity: 'base' });
          if (cmpResult !== 0) return cmpResult;
        }

        // 4. Shorter path first if one is a prefix of the other.
        cmpResult = aClasses.length - bClasses.length;
        if (cmpResult !== 0) return cmpResult;

        // 5. Final tie-breaker.
        return a[2].localeCompare(b[2], undefined, { sensitivity: 'base' });
      });

      // Change to alphabetic buckets if needed
      if (theClasses[2].length > CONSTS.VAL.BUCKET_THRESHOLD) {
        theClasses[2] = putInAlphabeticBuckets(theClasses[2]);
      }
    }

    // Class Members
    if (found.classMems && found.classMems[2]) {
      const converted = convertBranch(found.classMems[2], 'Classes > Class Members');
      if (converted.length > 0) theClasses[2].push(['[Members]', null, converted]);
    }

    // Class Hierarchy
    if (found.classHierarchy) theClasses[2].push(['[Hierarchy]', null, found.classHierarchy[1]]);

    // Class Index
    if (found.classIndex) theClasses[2].push(['[Index]', null, found.classIndex[1]]);

    // File List
    if (found.fileList) {
      theProject[2].push(['File List', null, found.fileList[1]]);
      theFiles[2] = convertBranch(found.fileList[2], 'Files > File List', /_8(hpp|h|cpp|c)$/);
    }

    // File Members
    if (found.fileMems && found.fileMems[2]) {
      const converted = convertBranch(found.fileMems[2], 'Files > File Members');
      if (converted.length > 0) theFiles[2].push(['[Members]', null, converted]);
    }

    // Example List
    if (found.examples) {
      theProject[2].push(['Examples List', null, found.examples[1]]);
      theExamples[2] = convertBranch(found.examples[2], 'Examples', /-example$/);
    }

    // Dev Hub
    if (found.devHub) {
      seenIgnoredHref.add(found.devHub[1]);
      ignoredList.push([found.devHub[0], found.devHub[1]]);
      theDevHub[2] = convertBranch(found.devHub[2], 'Dev Hub', /^dp_dh_/);
    }

    // Dev Hub Required Page
    if (found.dhRequired) {
      // splice(1, 0, 'bla') inserts 'bla' at position 1, 0 = removes nothing 
      if (theDevHub[2].length > 0) theDevHub[2].splice(1, 0, [found.dhRequired[0], null, found.dhRequired[1]]);
      else theDevHub[2].push([found.dhRequired[0], null, found.dhRequired[1]]);
    }

    // Deprecated List
    if (found.deprList) theProject[2].push(['Deprecated List', null, found.deprList[1]]);

    // Bug List
    if (found.bugsList) theProject[2].push(['Bug List', null, found.bugsList[1]]);

    // Todo List
    if (found.todoList) theProject[2].push(['Todo List', null, found.todoList[1]]);

    // Test List
    if (found.testList) theProject[2].push(['Test List', null, found.testList[1]]);

    // Assigning to vars.htmlData array
    if (theProject[2].length > 0) vars.htmlData.push(theProject);
    if (theDependencies[2].length > 0) vars.htmlData.push(theDependencies);
    if (theNamespaces[2].length > 0) vars.htmlData.push(theNamespaces);
    if (theConcepts[2].length > 0) vars.htmlData.push(theConcepts);
    if (theClasses[2].length > 0) vars.htmlData.push(theClasses);
    if (theFiles[2].length > 0) vars.htmlData.push(theFiles);
    if (theExamples[2].length > 0) vars.htmlData.push(theExamples);
    if (theDevHub[2].length > 0) vars.htmlData.push(theDevHub);

    // Check structure and normalize names. Since Doxygen changes
    // '&' to '&amp;', '<' to '&lt;', we have to change it back.
    checkData('html');

    // Save HTML data and ignored list
    storage.htmlData.save(vars.htmlData);
    storage.htmlIgnored.save(ignoredList);

    // Save not found list if it is non-empty
    if (htmlNotFound.length > 0) storage.htmlNotFound.save(htmlNotFound);
    else storage.htmlNotFound.remove();

    // Check for redirection, if true then return
    if (checkRedirect(vars.htmlData)) return;

    // Save the current page as prevHtml for future use
    storage.htmlPrevName.save(CONSTS.HTML_NAME);

    // Assign dev hub class for detection in CSS
    if (vars.htmlData.length > 0 && vars.htmlData[vars.htmlData.length - 1][0] === 'Dev Hub') {
      document.body.classList.add(CONSTS.CLS.DEV_HUB_AVAILABLE);
    }

    // Deep freeze vars.htmlData
    deepFreeze(vars.htmlData);
  }

