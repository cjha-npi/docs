  // Deep-clone a nested array structure that contains only arrays and primitive values.
  function deepCloneArray(item) {
    if (!Array.isArray(item)) return item;
    const out = [];
    for (const node of item) out.push(deepCloneArray(node));
    return out;
  }

  // Replaces all occurrences of "anonymous_namespace{...}" with "<...>"
