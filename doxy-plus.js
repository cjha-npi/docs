  // Run the main initialization sequence once the DOM is ready.
  async function docOnReady() {
    // Build or load the root-pane HTML data first because:
    // 1. It handles url redirection, and
    // 2. Later steps depend on it.
    await initHtmlData();

    // Cache DOM elements after the HTML data is ready.
    populateElements();

    // Build or load the leaf-pane hash data after elements are populated,
    // because it may need already-cached DOM references.
    await initHashData();

    // Build the DOM trees for the custom navigation panes.
    // This is synchronous and expected to finish before later UI setup.
    buildTrees();

    // Initialize pane sizing, visibility state, and resizer behavior.
    // This is synchronous and depends on the elements already existing.
    initDualPane();

    // Start observer setup in parallel.
    // This is intentionally fire-and-forget:
    // - we do not want to block startup on it
    // - but we still catch rejections so they do not become unhandled promises
    void setupObservers().catch((err) => {
      console.error('docOnReady(): setupObservers() failed', err);
    });

    // Start search bar tweaks in parallel.
    // Same reasoning as above: do not block startup, but do handle async failure explicitly.
    void searchbarTweak().catch((err) => {
      console.error('docOnReady(): searchbarTweak() failed', err);
    });

    // Start options UI setup in parallel.
    // Again, this is intentionally not awaited, but any rejection is logged explicitly.
    void initOptions().catch((err) => {
      console.error('docOnReady(): initOptions() failed', err);
    });

    // Expose the debug/helper console object after core initialization has completed.
    // This stays synchronous and runs immediately.
    createConsoleObject();

    // Restore previous vertical scroll position for this page and save new
    // scroll positions as the user scrolls or when page hides.
    restoreAndObserveVScroll();
  }

  // Fires as soon as the browser has parsed the HTML and built the DOM (Document Object Model) tree — before images, 
  // stylesheets, iframes, etc., have all finished loading.
  // ⚠️ Fires much earlier than 'window.addEventListener('load', function ()...'
  // ⚠️ Guranteed that 'docOnReady()' will fire once DOM has been prased and is available 
  if (document.readyState === 'loading') {
    // The DOM is still being parsed.
    // Register a one-time callback to start initialization as soon as DOMContentLoaded fires.
    document.addEventListener('DOMContentLoaded', () => {
      // Call the async initializer without awaiting it here because this callback itself is not async.
      // Attach .catch(...) so that any rejection from docOnReady() is handled explicitly.
      void docOnReady().catch((err) => {
        console.error('docOnReady() failed', err);
      });
    });
  } else {
    // The DOM is already available, so start initialization immediately.
    // Again, attach .catch(...) so startup failures are logged and do not become unhandled rejections.
    void docOnReady().catch((err) => {
      console.error('docOnReady() failed', err);
    });
  }

