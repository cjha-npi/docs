//
// TL;DR: In some edge cases, a custom hash can make the default
// side nav disappear.
//
// Doxygen uses NAVTREEINDEX to resolve a hash/href. NAVTREEINDEX is
// split across multiple files and stores entries as: root + hash
// (where "root" is the page URL ending with ".html").
//
// For a given hash/href, Doxygen looks up the root + hash combo in
// NAVTREEINDEX. If it cannot find that combo, it falls back to selecting
// just the root. This works fine with Doxygen's own hashes, but with a
// custom hash it can sometimes fail to find the root as well: while
// searching, it may advance to another NAVTREEINDEX file (because the
// index is split) that does not contain the root entry. In that case,
// Doxygen falls back to index.html, i.e. the "Main Project" item.
//
// This fallback is a serious problem for us because unlike the default
// sidenav, the sidenav generated by doxy-plus in this file does not have
// index.html as a single top level item. So, the fallback to index.html
// can give an incorrect sense of which page we currently are.
//
// ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
// #endregion ⚠️ WARNING: DO NOT CREATE CUSTOM HASH/HREF

// #region 🟩 STORE.JS INFO
// ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
//
// We are using the store.js file `store.everything.min.js`. It lets us store
// an expiry time with each key. The expiry is checked only when the key is
// accessed. If the current time is past the expiry time, store.js removes the
// stored data and returns `undefined`.
//
// To set the expiry time, we pass a third parameter to the `set()` function
// provided by store.js. This third parameter is the current time plus the time
// to live in milliseconds. For example:
// `store.set(key, val, Date.now() + 1000)`
//
// This stores `val` under `key` for 1000 milliseconds from now. The key is not
// removed immediately when it expires. It is removed only the next time it is
// accessed after the expiry time has passed.
//
// How the data is stored:
// In localStorage, store.js stores the same logical key using two separate
// keys when using a namespace with expiry support. For example, a key named
// `html_pages` may be stored as:
// `__storejs_F-Doxy-Main_html_pages`
// `__storejs___storejs_F-Doxy-Main_expire_mixin_html_pages`
//
// Using this knowledge, we can iterate through all keys in localStorage and
// extract the namespace name and key name from the second key above. The first
// key format does not give us a clear separator between the namespace name and
// the key name, but the second key does. Its format is:
// `__storejs___storejs_<namespace name>_expire_mixin_<key name>`
//
// So, when iterating through localStorage, we only inspect keys that start
// with `__storejs___storejs_` and then check whether they contain the
// separator `_expire_mixin_`. If both conditions are true, we can extract the
// namespace name and key name.
//
// This lets us find all keys related to any project, as long as they are under
// the same domain, or all local files if no URL is involved. If we want to
// remove an expired key, we simply access it through its namespace name and
// key name. If it has expired, store.js removes it automatically.
//
// ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
// #endregion 🟥 STORE.JS INFO

// #region 🟩 DATE-TIME ISSUE
