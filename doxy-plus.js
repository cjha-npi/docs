  // Restore a page's scroll position on load and save its scroll position on scroll and page hide.
  function restoreAndObserveVScroll() {

    // vars.els.DOC_CONTENT has already been verified and waited for (if needed)
    // in the setupObservers() function, so no need to do that here, just
    // return if it is not there or if it is not connected.
    if (!vars.els.DOC_CONTENT || !vars.els.DOC_CONTENT.isConnected) return;

    // Timer ID used to debounce scroll-position saves.
    let saveScrollTimer = null;

    // Retry restoring until vars.els.DOC_CONTENT is tall enough or the retry limit is reached.
    function restoreScroll() {
      const savedTop = Number(storage.pageScrollVPos.load());
      if (!Number.isFinite(savedTop) || savedTop < 0) return;

      let tries = 0;

      function tryRestore() {
        vars.els.DOC_CONTENT.scrollTop = savedTop;
        const maxTop = vars.els.DOC_CONTENT.scrollHeight - vars.els.DOC_CONTENT.clientHeight;
        if (maxTop >= savedTop || tries >= 50) return;
        tries += 1;
        requestAnimationFrame(tryRestore);
      }

      tryRestore();
    }

    // Save the current vertical scroll position of vars.els.DOC_CONTENT.
    function saveScroll() {
      const val = vars.els.DOC_CONTENT.scrollTop;
      if (val > 0) storage.pageScrollVPos.save(val);
      else storage.pageScrollVPos.remove();
    }

    // Debounce scroll-triggered saves so storage is written only after scrolling settles.
    function onScroll() {
      window.clearTimeout(saveScrollTimer);
      saveScrollTimer = window.setTimeout(() => {
        saveScrollTimer = null;
        saveScroll();
      }, 200);
    }

    // Clear any pending debounce timer and save the current scroll position.
    function saveScrollNow() {
      window.clearTimeout(saveScrollTimer);
      saveScrollTimer = null;
      saveScroll();
    }

    // Restore initially
    restoreScroll();

    // Add listeners for saving
    vars.els.DOC_CONTENT.addEventListener('scroll', onScroll, { passive: true });
    window.addEventListener('pagehide', saveScrollNow);
  }

