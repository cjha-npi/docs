  // Replace the magnifier search icon with relevant text.
  async function searchbarTweak() {

    // Replace the search category icon with a text label that reflects the current search context.
    // By default, Doxygen shows only a magnifying-glass icon here.

    // Wait until test() returns true.
    async function waitUntil(test) {
      const start = performance.now();
      while (!test()) {
        const elapsed = performance.now() - start;
        if (elapsed > CONSTS.VAL.TIMEOUT_MS) return false;
        await new Promise(requestAnimationFrame);
      }
      return true;
    }

    // Readiness condition for update().
    const updateCondition = () => !!(
      window.searchBox &&
      typeof window.searchBox.searchIndex === 'number' &&
      window.indexSectionLabels
    );

    // Sync the '--dp-search-field' CSS variable based on the current search category.
    async function update() {
      const okToUpdate = updateCondition() || await waitUntil(updateCondition);
      if (!okToUpdate) {
        console.error('searchbarTweak() - update(): Timed out waiting for conditions');
        return;
      }
      const label = window.indexSectionLabels[window.searchBox.searchIndex] || 'All';
      document.documentElement.style.setProperty(CONSTS.PROP.SEARCH_FIELD, `"${label}:"`);
    }

    // Readiness condition for attaching to OnSelectItem.
    const attachCondition = () => !!(
      window.searchBox &&
      typeof window.searchBox.OnSelectItem === 'function'
    );

    // Ensure window.searchBox.OnSelectItem exists before hooking into it.
    const okToAttach = await waitUntil(attachCondition);
    if (!okToAttach) {
      console.error('searchbarTweak(): Timed out waiting for searchBox.OnSelectItem.');
      return;
    }

    // Override OnSelectItem to call update() whenever the user selects 
    // a different category from the dropdown.
    const orig = window.searchBox.OnSelectItem;
    window.searchBox.OnSelectItem = function (id) {
      const ret = orig.call(this, id);
      //console.log('searchbarTweak(): Update On Select Item Triggered');
      void update().catch((err) => {
        console.error('searchbarTweak(): update() failed', err);
      });
      return ret;
    };
    //console.log('searchbarTweak(): SUCCESS');

    // Run one initial sync.
    await update();
  }

