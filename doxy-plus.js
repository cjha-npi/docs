  // Returns a fully loaded, pruned clone of Doxygen NAVTREE, or [] on timeout.
  async function buildPrunedDoxygenNavTree() {
    // Steps:
    // 1. Wait until window.NAVTREE exists and window.NAVTREE[0][2] is a non-empty array (timeout after CONSTS.VAL.TIMEOUT_MS).
    // 2. Recursively clone the tree structure into a new array.
    // 3. Dynamically load any external JS files for deferred child nodes.
    // 4. Recursively attach those loaded children.
    // 5. Prune hash entries from the cloned NAVTREE, e.g. remove links like "file.html#anchor".
    // 6. Return the pruned clone.

    // Wait for NAVTREE
    const start = performance.now();
    while (!Array.isArray(window.NAVTREE) || !Array.isArray(window.NAVTREE?.[0]?.[2]) || window.NAVTREE[0][2].length === 0) {
      const elapsed = performance.now() - start;
      if (elapsed > CONSTS.VAL.TIMEOUT_MS) {
        dispFatalError(`buildPrunedDoxygenNavTree(): Timed out waiting for a non-empty "window.NAVTREE[0][2]" after ${Math.round(elapsed)} ms`);
        return [];
      }
      //console.log(`buildPrunedDoxygenNavTree(): Waiting for non-empty "window.NAVTREE[0][2]" after ${Math.round(elapsed)} ms...`);
      await new Promise((resolve) => { setTimeout(resolve, 16); });
    }

    // Load a script only once and share the same promise across callers.
    function loadScript(relUrl) {
      // - Build one absolute URL so different relative forms map to one key.
      // - Return the existing promise when the same script was already requested.
      // - Remove the failed entry from the cache so a later retry is possible.

      // Build the absolute URL.
      const fullUrl = new URL(relUrl, CONSTS.DOC_ROOT).href;

      // Reuse the existing promise when this absolute URL was already requested.
      const existingPromise = vars.scriptLoadMap.get(fullUrl);
      if (existingPromise) return existingPromise;

      const promise = new Promise((resolve, reject) => {
        // Create and configure a script element.
        const s = document.createElement('script');
        s.src = fullUrl;
        s.async = true;

        // when the script finishes loading, call resolve()
        s.onload = () => { resolve(); };

        // if the script fails to load, delete from map, remove it and call reject(err)
        s.onerror = (err) => {
          vars.scriptLoadMap.delete(fullUrl);
          s.remove();
          reject(err);
        };

        // Insert the script element into the page and start the request.
        document.head.appendChild(s);
      });

      // Add script to the map and return the promise
      vars.scriptLoadMap.set(fullUrl, promise);
      return promise;
    }

    // Walks the cloned tree, loads deferred children, and replaces string refs
    function loadChildren(branch) {
      if (!Array.isArray(branch) || branch.length === 0) return Promise.resolve();
      const promises = [];
      for (const node of branch) {
        if (!Array.isArray(node)) continue;
        const h = node[1]; // Get the href
        const c = node[2]; // Child reference or child array.
        if (typeof c === 'string' && c.length > 0) {

          // c is a script base name: load it then replace node[2] with the loaded array
          // Check if the h is an anchor link (contains '#').
          // Inlined elements link to anchors, not new pages.
          const isAnchorLink = h && h.includes('#');

          // WARNING: Do not remove this check for anchor link. For inlined element
          // (e.g., INLINE_SIMPLE_STRUCTS = YES) there is still a script in node[2]
          // but loading that script leads to errors because the file it points to
          // does not exist.
          if (isAnchorLink) {
            // This is an inlined element (e.g., INLINE_SIMPLE_STRUCTS = YES).
            // It won't have a separate .js file for children.
            // Set children to an empty array and skip loading.
            node[2] = [];
          } else {
            // This is a regular page, load the script as before.
            // c is a script base name: load it then replace node[2] with the cloned loaded array
            promises.push(
              loadScript(c + '.js')
                .then(() => {
                  let arr = window[c];
                  if (!Array.isArray(arr)) arr = window[c.split('/').pop()];

                  if (!Array.isArray(arr)) {
                    console.warn(`buildPrunedDoxygenNavTree() - loadChildren(): "${c}.js" loaded, but no valid array global was found for "${c}"`);
                    node[2] = [];
                    return;
                  }

                  node[2] = deepCloneArray(arr);
                  return loadChildren(node[2]);
                })
                .catch((err) => {
                  console.warn(`buildPrunedDoxygenNavTree() - loadChildren(): Failed to resolve children for "${c}.js"`, err);
                  node[2] = [];
                })
            );
          }
        }
        else if (Array.isArray(c) && c.length > 0) {
          // Already an array: recurse
          promises.push(loadChildren(c));
        }
      }
      return Promise.all(promises);
    }

    // Clone the default Doxygen NAVTREE
    const clonedTree = deepCloneArray(window.NAVTREE);

    // Load all deferred children; not all NAVTREE data is present up front.
    await loadChildren(clonedTree);

    // Prune cloned tree in place.
    (function pruneClonedTree(branch) {
      if (!Array.isArray(branch) || branch.length === 0) return;

      for (let ii = branch.length - 1; ii >= 0; --ii) {

        // Remove nodes that are not arrays
        if (!Array.isArray(branch[ii])) {
          branch.splice(ii, 1);
          continue;
        }

        // Normalize length to 3 entries only
        branch[ii].length = 3;

        // Remove nodes with empty names
        if (typeof branch[ii][0] !== 'string' || branch[ii][0].length === 0) {
          branch.splice(ii, 1);
          continue;
        }

        // Prune child nodes if present.
        pruneClonedTree(branch[ii][2]);
        if (!Array.isArray(branch[ii][2]) || branch[ii][2].length === 0) branch[ii][2] = null;

        // Validate href: keep only valid html links, otherwise set to null
        if (typeof branch[ii][1] !== 'string' || branch[ii][1].includes('#') || !/\.html$/i.test(branch[ii][1])) branch[ii][1] = null;

        // Remove nodes that have no href and no kids
        if (branch[ii][1] === null && branch[ii][2] === null) branch.splice(ii, 1);
      }
    })(clonedTree);

    //console.log('buildPrunedDoxygenNavTree(): SUCCESS');
    return clonedTree;
  }

