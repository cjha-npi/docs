  // Compares two strings and provides highest priority to alphabets.
  function compareAlphaBeforePunctuation(a, b) {
    // Provided for sorting anonymous namespace after normal normal ones.
    // Anonymous namespace is often replaced using 'prettierAnon()' function
    // above. Then the sorting happens and so normal namespace that starts
    // with alphabets will be sorted above anonymous namespaces. For example
    // 'some_ns' will be above '<anon_ns>'.

    if (typeof a !== 'string' || typeof b !== 'string') return 0;

    const aLen = a.length;
    const bLen = b.length;
    const minLen = Math.min(aLen, bLen);

    // Compare each character till the length of the shorter string
    for (let ii = 0; ii < minLen; ++ii) {
      const aCh = a[ii].toUpperCase();
      const bCh = b[ii].toUpperCase();

      const aIsAlpha = (aCh >= 'A' && aCh <= 'Z');
      const bIsAlpha = (bCh >= 'A' && bCh <= 'Z');

      if (aIsAlpha && !bIsAlpha) return -1;
      if (!aIsAlpha && bIsAlpha) return 1;

      // If both characters are alphabets then use native compare
      const cmpResult = aCh.localeCompare(bCh, undefined, { sensitivity: 'base' });
      if (cmpResult !== 0) return cmpResult;
    }

    // In case of one string is a sub-string of another, e.g. a = 'Some' and
    // b = 'SomeFunc', then sort shorter string before longer string.
    return aLen - bLen;
  }

  // Recursively freezes the given value and everything nested inside it.
  function deepFreeze(value) {
    // Why this is needed:
    // Object.freeze(...) is shallow. It only freezes the current object/array itself,
    // not the nested objects or arrays stored inside it. So if an object contains
    // another object or array, those inner values would still remain mutable unless
    // they are also frozen explicitly.
    //
    // What this function does:
    // 1. Checks whether the passed value is a non-null object/array.
    // 2. Skips values that are primitives (string, number, boolean, null, etc.).
    // 3. Skips values that are already frozen to avoid useless repeated work.
    // 4. Freezes the current object/array itself.
    // 5. Recursively walks through all nested children and freezes them too.
    //
    // For arrays:
    // It iterates through each item and recursively freezes that item.
    //
    // For plain objects:
    // It gets all own property names using Object.keys(...) and recursively freezes
    // the value stored in each property.
    //
    // Result:
    // After this function completes, the full reachable object/array tree is frozen,
    // not just the top-level container.
    //
    // Important:
    // - This only works for values reachable from the passed root.
    // - This is runtime protection, not compile-time enforcement.
    // - In strict mode, illegal mutations throw errors. Without strict mode, some
    //   mutation attempts may fail silently.
    if (value && typeof value === 'object' && !Object.isFrozen(value)) {
      // Freeze the current object/array first.
      Object.freeze(value);

      if (Array.isArray(value)) {
        // For arrays, recurse into each element.
        for (const item of value) {
          deepFreeze(item);
        }
      } else {
        // For objects, Object.keys(value) returns an array of this object's own
        // property names, e.g. ['label', 'scope', 'value'].
        for (const key of Object.keys(value)) {
          // value[key] gets the actual value stored in that property, and that
          // nested value must also be frozen if it is an object/array.
          deepFreeze(value[key]);
        }
      }
    }

    return value;
  }

  // Waits for the first DOM element that matches selector.
  function waitFor(selector) {
    // Returns a Promise that:
    // 1. resolves with the matching element as soon as it is found,
    // 2. resolves with null if the timeout is reached first,
    // 3. rejects if selector is not a valid querySelector() string.
    //
    // Behavior:
    // 1. Check immediately in case the element already exists.
    // 2. If not found, watch DOM changes and check again after each mutation.
    // 3. Stop watching as soon as the element is found or the timeout expires.

    return new Promise((resolve, reject) => {
      let timer;
      let obs;

      // Function for cleaning up
      function cleanup() {
        if (obs) obs.disconnect();
        clearTimeout(timer);
      }

      // Function to find the selector
      function findNow() {
        try {
          const el = document.querySelector(selector);
          if (!el) return null;
          cleanup();
          //console.log(`waitFor(): Found selector "${selector}"`, el);
          resolve(el);
          return el;
        } catch (err) {
          cleanup();
          console.error(`waitFor(): invalid selector "${selector}"`, err);
          reject(err);
          return null;
        }
      }

      // Check once before starting the observer.
      if (findNow()) return;

      // Create a mutation observer that calls findNow on mutations
      obs = new MutationObserver(() => { findNow(); });

      // Observe a root node that exists even if body is not available yet.
      obs.observe(document.documentElement || document, { childList: true, subtree: true });

      // Stop waiting after the timeout.
      timer = setTimeout(() => {
        cleanup();
        console.warn(`waitFor(): Timed out waiting for selector "${selector}" after ${CONSTS.VAL.TIMEOUT_MS} ms`);
        resolve(null);
      }, CONSTS.VAL.TIMEOUT_MS);
    });
  }

  // Check correct construction of html/hash data and decode escaped page titles.
  function checkData(kind = 'html') {

    const isHtml = kind === 'html';
    const isHash = kind === 'hash';

    if (!isHtml && !isHash) dispFatalError(`checkData(): Invalid kind "${kind}".`);

    const data = isHtml ? vars.htmlData : vars.hashData;
    const dataName = isHtml ? 'HTML Data' : 'Hash Data';
    const dataVarName = isHtml ? 'vars.htmlData' : 'vars.hashData';
    const nodeLen = isHtml ? 3 : 2;
    const valIdx = isHtml ? 2 : 1;
    const finalEntry = isHtml ? 'Third' : 'Second';

    if (!Array.isArray(data)) {
      dispFatalError(`checkData(): Top-level ${dataName} (${dataVarName}) is missing or invalid.`);
    }

    if (data.length === 0) {
      dispFatalError(`checkData(): Top-level ${dataName} (${dataVarName}) is empty.`);
    }

    const decodeText = document.createElement('textarea');
    function checkBranch(branch, parentChain = '') {

      let idx = 0;
      for (const node of branch) {
        idx += 1;

        const rawPrefix = parentChain
          ? `checkData(): ${dataName} Node [${dataVarName} > ${parentChain} > Node ${idx}] `
          : `checkData(): ${dataName} Node [${dataVarName} > Top Level Node ${idx}] `;

        if (!Array.isArray(node)) {
          dispFatalError(`${rawPrefix} It is not an array.`);
        }

        if (node.length !== nodeLen) {
          dispFatalError(`${rawPrefix} Its length is not ${nodeLen}, it is ${node.length}.`);
        }

        if (typeof node[0] !== 'string') {
          dispFatalError(`${rawPrefix} First entry (i.e. name) is not a string, it is ${typeof node[0]}.`);
        }

        // Doxygen changes certain characters like '&' to '&amp;', '<' to '&lt;', etc.
        // To change it back we use the below method.
        if (node[0].includes('&')) {
          decodeText.innerHTML = node[0];
          node[0] = decodeText.value;
        }

        if (node[0].trim().length === 0) {
          dispFatalError(`${rawPrefix} First entry (i.e. name) is an empty or whitespace-only string.`);
        }

        const labelPrefix = parentChain
          ? `checkData(): ${dataName} Node [${dataVarName} > ${parentChain} > ${idx}: ${node[0]}] `
          : `checkData(): ${dataName} Node [${dataVarName} > ${idx}: ${node[0]}] `;

        if (isHtml) {
          if (typeof node[1] !== 'string' && node[1] !== null) {
            dispFatalError(`${labelPrefix} Second entry (i.e. prefix) is of invalid type, it is ${typeof node[1]}.`);
          }

          if (typeof node[1] === 'string' && node[1].trim().length === 0) {
            dispFatalError(`${labelPrefix} Second entry (i.e. prefix) is an empty or whitespace-only string.`);
          }
        }

        if (typeof node[valIdx] === 'string') {
          if (node[valIdx].trim().length === 0) {
            dispFatalError(`${labelPrefix} ${finalEntry} entry (i.e. link or kids) is an empty or whitespace-only string.`);
          }
        }
        else if (Array.isArray(node[valIdx])) {
          if (node[valIdx].length === 0) {
            dispFatalError(`${labelPrefix} ${finalEntry} entry (i.e. link or kids) is an empty child branch.`);
          }

          const fullNameChain = parentChain
            ? `${parentChain} > ${idx}: ${node[0]}`
            : `${idx}: ${node[0]}`;

          checkBranch(node[valIdx], fullNameChain);
        }
        else {
          dispFatalError(`${labelPrefix} ${finalEntry} entry (i.e. link or kids) is of invalid type, it is ${typeof node[valIdx]}.`);
        }
      }
    }

    checkBranch(data);
  }

