  // #endregion 🟥 INIT HTML DATA

  // #region 🟩 POPULATE ELEMENTS
  // ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

  // Find elements from CONSTS.SEL and store them in vars.els
  function populateElements() {
    Object.keys(vars.els).forEach((key) => { delete vars.els[key]; });
    Object.assign(
      vars.els,
      Object.fromEntries(
        Object.entries(CONSTS.SEL).map(([key, selector]) => [key, document.querySelector(selector)])
      )
    );
  }

  // ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
  // #endregion 🟥 POPULATE ELEMENTS

  // #region 🟩 INIT HASH DATA
  // ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

  // Initialize vars.hashData array
  async function initHashData() {

    // Reset the hash data before rebuilding it, while preserving its reference
    vars.hashData.length = 0;

    // Load the stored hash data for this page
    const prevHashData = storage.hashData.load();

    // If stored hash data is explicitly empty then return
    if (prevHashData === 'Empty') return;

    // If stored hash data is a non-empty array then assign and return
    if (Array.isArray(prevHashData) && prevHashData.length > 0) {
      document.body.classList.add(CONSTS.CLS.HASH_DATA_AVAILABLE);
      const isCode = storage.hashDataIsCode.load();
      if (isCode === 'yes') {
        document.body.classList.add(CONSTS.CLS.HASH_DATA_IS_CODE);
      }
      vars.hashData.push(...prevHashData);
      deepFreeze(vars.hashData);
      return;
    }

    // Ensure vars.els.PAGE_CONTENT exists and is still connected before generating hash data.
    if (!vars.els.PAGE_CONTENT || !vars.els.PAGE_CONTENT.isConnected) {
      vars.els.PAGE_CONTENT = await waitFor(CONSTS.SEL.PAGE_CONTENT);
      if (!vars.els.PAGE_CONTENT) {
        console.warn(`initHashData(): Selector ${CONSTS.SEL.PAGE_CONTENT} not found; hash data could not be generated.`);
        return;
      }
    }

    // Formats signatures for C++ types
    function formatSignature(text) {
      if (typeof text !== 'string') return text;

      return text
        // Remove space before *, &, &&
        .replace(/\s+([*&]{1,2})/g, '$1')

        // Ensure space after *, &, &&
        .replace(/([*&]{1,2})(?!\s)/g, '$1 ')

        // Remove spaces inside <...>
        .replace(/<\s+/g, '<')
        .replace(/\s+>/g, '>')
        .replace(/\s+<\s+/g, '<')
        .replace(/\s+>\s+/g, '>')

        // Remove space before commas, ensure one after
        .replace(/\s+,/g, ',')
        .replace(/,(?!\s)/g, ', ')

        // Remove space after ( and before )
        .replace(/\(\s+/g, '(')
        .replace(/\s+\)/g, ')')

        // Remove space before (
        .replace(/\s+\(/g, '(')

        // Add space before and after = in special cases
        .replace(/\s*=\s*(default|delete|0)/g, ' = $1')

        // Collapse multiple spaces and trim
        .replace(/\s{2,}/g, ' ')

        // leading "}"
        .replace(/^\}+\s*/, '')

        // trailing "{"
        .replace(/\s*\{+$/, '')

        .trim();
    }

    // Returns members hash for doxygen generated 'table.memberdecls'.
    // NOT for markdown pages.
    // The '<tbody>' of '<table class="memberdecls">' inside '<div class=contents">'
    // constains the list of members. This list of members usually looks like
    // '<tr class="memitem:abc" id="r_abc">' and this is what is passed as 'row' to
    // this function. In this row the 'abc' part is the hash.
    // Sometimes (for templates usually) there is no 'id="r_abc"', instead the class
    // name contains 'template' e.g. 'class="memitem:abc template"'. So, in order to
    // get the hash we get the 'abc' from 'id="r_abc"' and then use 'row.classList'
    // which splits on space and then if a class text from classList starts with
    // 'memitem:' we split that and take the rest.
    // If we get hash from both classList and id and both hash do not match then we
    // log a console warning because this is inconsistent behavior.
    // For returning the hash, the hash gotten by 'id="r_abc"' is given priority over
    // hash gotten by classList. If neither generates a hash this function returns an
    // empty string.
    function getMemberHash(row) {
      if (!row) return '';
      let idHash = '';
      let classHash = '';

      // Get the hash from id
      if (typeof row.id === 'string' && row.id.startsWith('r_') && row.id.length > 2) {
        idHash = row.id.slice(2);
      }

      // Get the hash from classList
      for (const cls of row.classList) {
        if (cls.startsWith('memitem:') && cls.length > 8) {
          classHash = cls.slice(8);
          break;
        }
      }

      // If both id and classHash hash is generated then they must match
      if (idHash && classHash && idHash !== classHash) {
        console.warn('initHashData() - getMemberHash(): row id/class hash mismatch', row, idHash, classHash);
      }

      // return priority: idHash > classHash.
      return idHash || classHash || '';
    }

    // Collect member signatures and group them into vars.hashData by section header.
    // NOT for markdown pages.
    // The '<tbody>' of '<table class="memberdecls">' inside '<div class=contents">'
    // constains the list of members. This list of members usually looks like
    // '<tr class="memitem:abc" id="r_abc">' and this is what is use as 'row' in
    // this function.
    const tables = Array.from(vars.els.PAGE_CONTENT.querySelectorAll("table.memberdecls"));
    tables.forEach((table, idx) => {
      const grpSigs = [];
      const seenName = new Set();
      const seenHref = new Set();

      const headerEl = table.querySelector("h2.groupheader");
      if (!headerEl) return;

      let headName = (headerEl.textContent || '').replace(/\s+/g, ' ').trim();
      if (headName.length === 0) headName = `Members ${idx + 1}`;
      headName = prettierAnon(headName);

      const rows = table.querySelectorAll('tr');
      rows.forEach((row) => {
        const leftTd =
          row.querySelector(':scope > td.memItemLeft, :scope > td.memTemplItemLeft');
        const rightTd =
          row.querySelector(':scope > td.memItemRight, :scope > td.memTemplItemRight');

        if (!leftTd || !rightTd) return;

        const memberHash = getMemberHash(row);
        if (memberHash.length === 0) return;

        const leafHref = `#${memberHash}`;
        if (seenHref.has(leafHref)) return;

        const lftText = leftTd.innerText.replace(/\s+/g, ' ').trim();
        const ritText = rightTd.innerText.replace(/\s+/g, ' ').trim();

        const leafNameTemp = `${lftText} ${ritText}`.trim();
        let leafName = formatSignature(leafNameTemp);

        if (leafName.startsWith('enum')) {
          leafName = leafName.replace(/\s*\{[\s\S]*\}/, '').trim();
        }

        leafName = prettierAnon(leafName);
        if (seenName.has(leafName)) return;

        seenHref.add(leafHref);
        seenName.add(leafName);
        grpSigs.push([leafName, leafHref]);
      });

      // Add this group to hash data if it has entries
      if (grpSigs.length > 0) {
        vars.hashData.push([headName, grpSigs]);
      }
    });

    // The hash data was generated using member table, this means
    // we are on a page with code. Set the correct class lists,
    // save the data, freeze vars.hashData and return.
    if (vars.hashData.length > 0) {
      checkData('hash'); // Checks structure and normallizes names
      document.body.classList.add(CONSTS.CLS.HASH_DATA_AVAILABLE);
      document.body.classList.add(CONSTS.CLS.HASH_DATA_IS_CODE);
      storage.hashData.save(vars.hashData);
      storage.hashDataIsCode.save('yes');
      deepFreeze(vars.hashData);
      return;
    }

    // Return the numeric heading level for a Doxygen Markdown section heading.
    function getDoxSectionLevel(el) {
      if (!el || typeof el.tagName !== 'string') return 0;
      const m = /^H([1-6])$/i.exec(el.tagName);
      return m ? Number(m[1]) : 0;
    }

    // Return the anchor id for a Doxygen Markdown section heading.
    function getDoxSectionAnchorId(el) {
      if (!el) return '';

      // Normal case:
      // <h2 class="doxsection"><a class="anchor" id="..."></a>Title</h2>
      const a = el.querySelector(':scope > a.anchor[id], :scope > a[id]');
      if (a && a.id) return a.id;

      // Fallback: id on the heading itself.
      if (el.id) return el.id;

      // Fallback: anchor immediately before the heading.
      const prev = el.previousElementSibling;
      if (prev && prev.matches && prev.matches('a.anchor[id], a[id]') && prev.id) {
        return prev.id;
      }

      return '';
    }

    // Get on-page heading anchors for Markdown pages.
    // NOTE: A parent heading loses its own anchor as soon as it gets a child.
    const doxSections = Array.from(vars.els.PAGE_CONTENT.querySelectorAll('.doxsection'));
    const doxSeenHref = new Set();
    const doxStack = []; // { level: number, node: [name, href|kids] }

    for (const sec of doxSections) {
      const level = getDoxSectionLevel(sec);
      if (level === 0) continue;

      const id = getDoxSectionAnchorId(sec);
      if (!id) continue;

      let name = (sec.textContent || '').replace(/\s+/g, ' ').trim();
      if (name.length === 0) continue;
      name = prettierAnon(name);

      const href = `#${id}`;
      if (doxSeenHref.has(href)) continue;
      doxSeenHref.add(href);

      const node = [name, href];

      while (doxStack.length > 0 && doxStack[doxStack.length - 1].level >= level) {
        doxStack.pop();
      }

      if (doxStack.length === 0) {
        vars.hashData.push(node);
      } else {
        const parent = doxStack[doxStack.length - 1].node;

        // By design, once a heading gets children, its own hash is discarded.
        if (!Array.isArray(parent[1])) parent[1] = [];
        parent[1].push(node);
      }

      doxStack.push({ level, node });
    }

    // Hash data was generated using the 'doxsection' method for
    // markdown pages. In this case we do not set the class and
    // data for 'hashDataIsCode'. Just save the class list and
    // data for hash data, freeze vars.hashData and return.
    if (vars.hashData.length > 0) {
      checkData('hash'); // Checks structure and normallizes names
      document.body.classList.add(CONSTS.CLS.HASH_DATA_AVAILABLE);
      storage.hashData.save(vars.hashData);
      deepFreeze(vars.hashData);
      return;
    }

    // If we are here this means vars.hashData is still empty.
    // We store the empty sentinel explicitly for hash data 
    // so that next reload of this page does not try to regenrate 
    // the hash data and arrive at the same conclusion.
    storage.hashData.save('Empty');
  }

  // ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
  // #endregion 🟥 INIT HASH DATA

  // #region 🟩 BUILD TREES
  // ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

  // Build <ul>...<li> trees from vars.htmlData and vars.hashData
  function buildTrees() {

    // Class list
    const CLS = {
      list: 'dpt-list',      // ul
      node: 'dpt-node',      // li, tree node
      row: 'dpt-row',        // clickable row (<button> for branch, <a> for leaf)
      toggle: 'dpt-toggle',  // branch toggle icon
      prefix: 'dpt-prefix',  // prefix, e.g. spe::
      label: 'dpt-label',    // visible node text
      sep: 'dpt-sep',        // Separator i.e. span

      leaf: 'dpt-node--leaf',         // Node with no children
      branch: 'dpt-node--branch',     // Node with children
      current: 'dpt-node--current',   // Current node
      visited: 'dpt-node--visited',   // Visited node (only for hashTree)
      expanded: 'dpt-node--expanded', // Expanded branch node
    };

    // Map from hash href to its constructed leaf node.
    const hashToNode = new Map();

    // Handle only expand/collapse clicks; links work automatically.
    function clickHandler(evt) {

      // Find the target
      const target = evt.target instanceof Element ? evt.target : null;
      if (!target) return;

      // Return unless the clicked row is a branch button.
      const row = target.closest(`.${CLS.row}`);
      if (!(row instanceof HTMLButtonElement)) return;

      // Find the node
      const li = row.closest(`.${CLS.node}`);
      if (!li || !li.classList.contains(CLS.branch)) return;

      // Toggle its expanded state
      const isExpanded = li.classList.toggle(CLS.expanded);
      row.setAttribute('aria-expanded', isExpanded ? 'true' : 'false');
    }

    // Mark a node as current and expand its parent chain.
    function markAndExpandCurrentNode(curNode) {
      if (!curNode) return;
      curNode.classList.add(CLS.current);
      let curPar = curNode.parentElement ? curNode.parentElement.closest(`.${CLS.node}`) : null;
      while (curPar) {
        if (curPar.classList.contains(CLS.branch)) {
          curPar.classList.add(CLS.expanded);
          const row = curPar.firstElementChild;
          if (row instanceof HTMLButtonElement) row.setAttribute('aria-expanded', 'true');
        }
        curPar = curPar.parentElement ? curPar.parentElement.closest(`.${CLS.node}`) : null;
      }
    }

    // Handle on-page hash changes.
    function onHashChange() {

      // Find previous current node first
      const prev = vars.els.DP_LEAF_SHOW.querySelector(`.${CLS.node}.${CLS.current}`);

      // Read the current hash
      const hash = window.location.hash;

      // If hash is empty then clear previous current and stop
      if (!hash) {
        if (prev) {
          prev.classList.remove(CLS.current);
          prev.classList.add(CLS.visited);
        }
        return;
      }

      // Find node directly from map
      const item = hashToNode.get(hash) ?? null;

      // Clear previous item only if it is different from the new one
      if (prev && prev !== item) {
        prev.classList.remove(CLS.current);
        prev.classList.add(CLS.visited);
      }

      // Return if new item is not found
      if (!item) return;

      // Clear visited state from the new item
      item.classList.remove(CLS.visited);

      // Mark it current and expand its ancestor path
      markAndExpandCurrentNode(item);

      // Scroll into view
      item.scrollIntoView({ behavior: 'smooth', block: 'nearest' });
    }

    // Build a tree from the given data and config.
    function buildTreeFromData(data, cfg) {
      if (!Array.isArray(data) || data.length === 0) return null;

      let curNode = null;

      // Create root <ul>.
      const root = document.createElement('ul');
      root.classList.add(CLS.list);

      // Stack used for iterative tree construction.
      const stack = [{ branch: data, parent: root }];
      while (stack.length) {

        const { branch, parent } = stack.pop();
        if (!Array.isArray(branch) || branch.length === 0) continue;

        const fragment = document.createDocumentFragment();

        for (let ii = 0; ii < branch.length; ++ii) {

          const item = branch[ii];

          // li for the node
          const li = document.createElement('li');
          li.classList.add(CLS.node);

          // Obtain prefix text (namespace/scope) and value (href or kids)
          const prefixText = cfg.getPrefixText(item);
          const value = cfg.getValue(item);

          // Create prefix as span if prefix text exists
          let prefix = null;
          if (prefixText) {
            prefix = document.createElement('span');
            prefix.classList.add(CLS.prefix);
            prefix.textContent = prefixText;
          }

          // We separate label based on '::'. This way we can
          // style '::' differently using css. Using normal
          // non-monospace font for `::` makes it too weak and
          // not visible and so we use monospace font for `::`
          // and normal font for the rest of the label.
          const labels = [];
          const labelText = cfg.getLabelText(item);
          const labelParts = labelText.split('::');
          for (const lblPart of labelParts) {
            if (labels.length > 0) {
              const sep = document.createElement('span');
              sep.classList.add(CLS.sep);
              sep.textContent = '::';
              labels.push(sep);
            }
            const label = document.createElement('span');
            label.classList.add(CLS.label);
            label.textContent = lblPart;
            labels.push(label);
          }

          // If value is of type string then this is a leaf node
          if (typeof value === 'string') {

            // Mark li as leaf
            li.classList.add(CLS.leaf);

            // Create row as a link
            const row = document.createElement('a');
            row.classList.add(CLS.row);
            row.href = cfg.getHref(item);
            row.title = cfg.getTitle(item);

            // Append prefix and labels to row
            if (prefix) row.append(prefix);
            for(const lbl of labels) row.append(lbl);

            // Append row to li
            li.appendChild(row);

            // Run the optional leaf callback.
            if (cfg.onLeaf) cfg.onLeaf(row, li);

            // Check for the node being the current node
            if (cfg.isCurrent(row)) curNode = li;
          }
          else if (Array.isArray(value) && value.length > 0) {
            // Value is non-empty array i.e. a branch

            // Mark li as a branch
            li.classList.add(CLS.branch);

            // Create row as a button for expand/collapse
            const row = document.createElement('button');
            row.classList.add(CLS.row);
            row.setAttribute('type', 'button');
            row.setAttribute('aria-expanded', 'false');
            row.title = cfg.getTitle(item);

            // Add toggle as span to show expand/collapse icon
            const toggle = document.createElement('span');
            toggle.classList.add(CLS.toggle);
            toggle.textContent = "\u2B9E"; // ⮞, rotated to show open state
            toggle.setAttribute('aria-hidden', 'true');

            // Append toggle, prefix and labels to row
            row.append(toggle);
            if (prefix) row.append(prefix);
            for(const lbl of labels) row.append(lbl);

            // Append row to li
            li.appendChild(row);

            // Create a ul sub-branch
            const twig = document.createElement('ul');
            twig.classList.add(CLS.list);
            li.appendChild(twig);

            // Queue the child branch and its parent <ul> for later processing.
            stack.push({ branch: value, parent: twig });
          }
          else {
            // Warn if the node is neither a leaf nor a branch. This should never happen.
            console.warn(`${cfg.warnName}: The following node is neither a leaf nor a branch.`, item);
            continue;
          }

          // Append the item <li> to its parent list <ul> fragment
          fragment.appendChild(li);
        }

        // Append fragment to parent <ul>
        parent.appendChild(fragment);
      }

      // Mark and expand the current node
      markAndExpandCurrentNode(curNode);

      // Return built tree
      return root;
    }

    function normalizePageHref(href) {
      let out = href.split('#')[0];
      if (out.endsWith('/')) out += 'index.html';
      return out;
    }

    const curHref = normalizePageHref(window.location.href);
    const curHash = window.location.hash;

    // Tree built from vars.htmlData
    const htmlTree = buildTreeFromData(vars.htmlData, {
      warnName: 'buildTreeFromData(): vars.htmlData',
      getLabelText: (item) => item[0],
      getPrefixText: (item) => item[1] ? `${item[1]}::` : null,
      getValue: (item) => item[2],
      getHref: (item) => CONSTS.DOC_ROOT + item[2],
      getTitle: (item) => item[1] ? `${item[1]}::${item[0]}` : item[0],
      isCurrent: (row) => normalizePageHref(row.href) === curHref,
    });

    // Tree built from vars.hashData
    const hashTree = buildTreeFromData(vars.hashData, {
      warnName: 'buildTreeFromData(): vars.hashData',
      getLabelText: (item) => item[0],
      getPrefixText: () => null,
      getValue: (item) => item[1],
      getHref: (item) => item[1],
      getTitle: (item) => item[0],
      isCurrent: (row) => row.getAttribute('href') === curHash,
      onLeaf: (row, li) => {
        const hash = row.getAttribute('href');
        if (hash) hashToNode.set(hash, li);
      },
    });

    // Mount htmlTree
    vars.els.DP_ROOT_PANE.replaceChildren();
    if (htmlTree) {
      vars.els.DP_ROOT_PANE.appendChild(htmlTree);
      htmlTree.addEventListener('click', clickHandler);
    }

    // Mount hashTree
    vars.els.DP_LEAF_SHOW.replaceChildren();
    if (hashTree) {
      vars.els.DP_LEAF_SHOW.appendChild(hashTree);
      hashTree.addEventListener('click', clickHandler);
      window.addEventListener('hashchange', onHashChange);
    }
  }

  // ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
  // #endregion 🟥 BUILD TREES

  // #region 🟩 INIT DUAL PANE
  // ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

  // Initialize the root and leaf panes and wire up their resizing behavior.
  function initDualPane() {

    // Timer ID for scheduling a save of both pane widths.
    let paneWidthSaveTimer = null;

    // Update and apply the root pane width.
    function setRootPaneWidth(w) {
      if (w !== vars.rootPaneWidth) {
        vars.rootPaneWidth = w;
        document.documentElement.style.setProperty(CONSTS.PROP.ROOT_PANE_WIDTH, `${w}px`);
      }
    }

    // Update and apply the leaf pane width.
    function setLeafPaneWidth(w) {
      if (w !== vars.leafPaneWidth) {
        vars.leafPaneWidth = w;
        document.documentElement.style.setProperty(CONSTS.PROP.LEAF_PANE_WIDTH, `${w}px`);
      }
    }

    // Return whether the leaf pane should be shown.
    function isLeafPaneActive() {
      return vars.hashData.length > 0
        && vars.showLeafPane
        && (!vars.autoHideLists || window.innerWidth > CONSTS.VAL.LEAF_PANE_AUTO_HIDE_THRESHOLD);
    }

    // Adjust pane widths on window resize in desktop view.
    function checkDualPaneLayout() {

      // Return if not in desktop view
      if (window.innerWidth < CONSTS.VAL.DESKTOP_VIEW_MIN_WIDTH) return;

      // Proceed only if the combined width exceeds the maximum allowed width.
      const maxAllowed = window.innerWidth - CONSTS.VAL.GUTTER_W;
      const combinedWidth = vars.rootPaneWidth + vars.leafPaneWidth;
      if (combinedWidth > maxAllowed) {

        // Calculate rebalanced widths.
        let nRoot = isLeafPaneActive() ?
          Math.floor(maxAllowed * (vars.rootPaneWidth / combinedWidth))
          : Math.min(vars.rootPaneWidth, maxAllowed - CONSTS.VAL.MIN_W);
        let nLeaf = maxAllowed - nRoot;

        // Enforce the minimum width for both panes.
        if (nRoot < CONSTS.VAL.MIN_W) {
          nLeaf = Math.max(CONSTS.VAL.MIN_W, maxAllowed - CONSTS.VAL.MIN_W);
          nRoot = CONSTS.VAL.MIN_W;
        }
        else if (nLeaf < CONSTS.VAL.MIN_W) {
          nRoot = Math.max(CONSTS.VAL.MIN_W, maxAllowed - CONSTS.VAL.MIN_W);
          nLeaf = CONSTS.VAL.MIN_W;
        }

        // Apply the rebalanced widths.
        setRootPaneWidth(nRoot);
        setLeafPaneWidth(nLeaf);

        // Schedule saving both pane widths.
        clearTimeout(paneWidthSaveTimer);
        paneWidthSaveTimer = setTimeout(() => {
          storage.rootPaneWidth.save(vars.rootPaneWidth);
          storage.leafPaneWidth.save(vars.leafPaneWidth);
          paneWidthSaveTimer = null;
        }, CONSTS.VAL.PANE_WIDTH_SAVE_DEBOUNCE_MS);
      }
    }

    // Wire drag-resize behavior for either the root or leaf pane.
    function wirePaneResizer(isRootResizer) {

      // State for the current drag session.
      let raf = null;
      let startX = 0;
      let startW = 0;
      let movBeg = 0;

      // Starting widths used to detect changes on pointerup.
      let prevRootWidth = vars.rootPaneWidth;
      let prevLeafWidth = vars.leafPaneWidth;

      // Apply the latest dragged width.
      function applyWidth() {
        const newW = startW + (movBeg - startX);
        let maxAllowed = window.innerWidth - CONSTS.VAL.GUTTER_W;
        if (isRootResizer) {
          // Adjust root pane width
          if (isLeafPaneActive()) {
            // If leaf pane is actively being shown then we cannot shrink its
            // size to let root pane width grow.
            maxAllowed -= vars.leafPaneWidth;
            const nVal = Math.max(CONSTS.VAL.MIN_W, Math.min(maxAllowed, newW));
            setRootPaneWidth(nVal);
          }
          else {
            // If leaf pane is not actively being shown then we can shrink its
            // size to let root pane width grow.
            maxAllowed -= CONSTS.VAL.MIN_W;
            const nVal = Math.max(CONSTS.VAL.MIN_W, Math.min(maxAllowed, newW));
            setRootPaneWidth(nVal);
            if (nVal + vars.leafPaneWidth > window.innerWidth - CONSTS.VAL.GUTTER_W) {
              setLeafPaneWidth(window.innerWidth - CONSTS.VAL.GUTTER_W - nVal);
            }
          }
        }
        else {
          // Adjust leaf pane width
          maxAllowed -= vars.rootPaneWidth;
          const nVal = Math.max(CONSTS.VAL.MIN_W, Math.min(maxAllowed, newW));
          setLeafPaneWidth(nVal);
        }

        // Mark that no frame is currently pending.
        raf = null;
      }

      // Handle pointer movement during drag.
      function onMove(evt) {
        evt.preventDefault();
        movBeg = evt.clientX;
        if (raf) return; // One queued frame is enough.
        raf = requestAnimationFrame(applyWidth);
      }

      // Finish dragging and apply the last pending width if needed.
      function onUp(evt) {
        evt.preventDefault();

        // Flush the final width if a frame is still pending.
        if (raf) {
          cancelAnimationFrame(raf);
          applyWidth();
        }

        // Save widths if they changed during this drag session.
        if (prevRootWidth !== vars.rootPaneWidth) storage.rootPaneWidth.save(vars.rootPaneWidth);
        if (prevLeafWidth !== vars.leafPaneWidth) storage.leafPaneWidth.save(vars.leafPaneWidth);

        document.body.style.cursor = '';
        document.removeEventListener('pointermove', onMove);
        document.removeEventListener('pointerup', onUp);
        document.removeEventListener('pointercancel', onUp);
      }

      const resizer = isRootResizer ? vars.els.DP_ROOT_RESIZER : vars.els.DP_LEAF_RESIZER;
      const getW = isRootResizer ? () => vars.rootPaneWidth : () => vars.leafPaneWidth;

      // Start dragging when the resizer is pressed.
      resizer.addEventListener('pointerdown', (evt) => {
        evt.preventDefault();
        startX = evt.clientX;
        startW = getW();
        prevLeafWidth = vars.leafPaneWidth;
        prevRootWidth = vars.rootPaneWidth;
        document.body.style.cursor = 'ew-resize';
        document.addEventListener('pointermove', onMove, { passive: false });
        document.addEventListener('pointerup', onUp, { passive: false });
        document.addEventListener('pointercancel', onUp, { passive: false });
      }, { passive: false });
    }

    // Apply the current stored widths before any interaction starts.
    document.documentElement.style.setProperty(CONSTS.PROP.ROOT_PANE_WIDTH, `${vars.rootPaneWidth}px`);
    document.documentElement.style.setProperty(CONSTS.PROP.LEAF_PANE_WIDTH, `${vars.leafPaneWidth}px`);

    // Wire both pane resizers.
    wirePaneResizer(true);
    wirePaneResizer(false);

    // Re-check layout whenever the window size changes.
    window.addEventListener('resize', checkDualPaneLayout);

    // Correct the layout once immediately on setup.
    checkDualPaneLayout();
  }

